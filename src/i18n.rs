#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Locale {
    En,
    It,
    Fr,
    De,
    Es,
    Pt,
}

impl Locale {
    pub fn from_code(code: &str) -> Self {
        match code.split(['-', '_']).next().unwrap_or("en") {
            "it" => Locale::It,
            "fr" => Locale::Fr,
            "de" => Locale::De,
            "es" => Locale::Es,
            "pt" => Locale::Pt,
            _ => Locale::En,
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Locale::En => "en",
            Locale::It => "it",
            Locale::Fr => "fr",
            Locale::De => "de",
            Locale::Es => "es",
            Locale::Pt => "pt",
        }
    }

    pub fn detect_system() -> Self {
        sys_locale::get_locale()
            .map(|l| Locale::from_code(&l))
            .unwrap_or(Locale::En)
    }
}

